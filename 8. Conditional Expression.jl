# If-Else-Elif

x=10
y=20
# using if-else
if x >= y
   print(x," is greater than ", y)
else
   print(y," is greater than ", x)
 end                        # condition will end here  
 
# using elif 
if x > y
   print(x," is greater than ", y)
elseif x < y
   print(y," is greater than ", x)
else
   print(x," is equal to ", y)
 end                         # condition will end here      
