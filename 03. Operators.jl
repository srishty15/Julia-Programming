# Basic Mathematic Operations
x=10
y=20
x += 10  # x=x+10;unary plus
x -= 10  # x=x-10;unary minus
x /= 10  # x=x/10;unary divide
x *= 10  # x=x*10;unary multiply
x + y    # binary plus
x - y    # binary subtraction
x * y    # product
x / y    # division
x \ y    # inverse division ie y/x
x % 4    # returns remainder
x ^ 2    # returns x to power 2

# Bitwise Operations
~x       # bitwise not
x & y    # bitwise and
x | y    # bitwise nor
xor(x,y) # bitwise xor
x >>> y  # logical shift right
x >> y   # arithmetic shift right
x << y	 # logical/arithmetic shift left
